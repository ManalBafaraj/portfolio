const state = {
  lang: localStorage.getItem("portfolio_lang") || "ar"
};

const navToggle = document.getElementById("navToggle");
const navPanel = document.getElementById("navPanel");
const langToggle = document.getElementById("langToggle");

function applyLanguage(lang) {
  state.lang = lang;
  const dict = translations[lang] ?? translations.ar;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.body.classList.toggle("en", lang === "en");

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const value = dict?.[key];
    if (value) element.textContent = value;
  });

  langToggle.textContent = lang === "ar" ? "EN" : "AR";
  localStorage.setItem("portfolio_lang", lang);
}

function closeMenu() {
  navPanel.classList.remove("open");
  navToggle.classList.remove("open");
  navToggle.setAttribute("aria-expanded", "false");
  navToggle.setAttribute("aria-label", "Open navigation");
  document.body.classList.remove("menu-open");
}

navToggle.addEventListener("click", () => {
  const isOpen = navPanel.classList.toggle("open");
  navToggle.classList.toggle("open", isOpen);
  navToggle.setAttribute("aria-expanded", String(isOpen));
  navToggle.setAttribute("aria-label", isOpen ? "Close navigation" : "Open navigation");
  document.body.classList.toggle("menu-open", isOpen);
});

navPanel.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", closeMenu);
});

langToggle.addEventListener("click", () => {
  applyLanguage(state.lang === "ar" ? "en" : "ar");
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") closeMenu();
});

document.addEventListener("click", (event) => {
  if (!navPanel.classList.contains("open")) return;
  if (navPanel.contains(event.target) || navToggle.contains(event.target)) return;
  closeMenu();
});

const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("visible");
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.14 });

document.querySelectorAll(".reveal").forEach((element) => revealObserver.observe(element));

// Ensure language is applied after DOM is ready
document.addEventListener("DOMContentLoaded", () => {
  applyLanguage(state.lang);
});
